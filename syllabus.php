					<h3><a name="courseDescription">Course Description</h3>


					<p>Digital media - like Instagram photos, TikTok stories, Google searches, Buzzfeed and Boing Boing articles, Reddit threads, and WhatsApp messages - combines to form the vast majority of the content most of us create and consume every day. Existing digital media and the digitized versions of the old media before it are available to more people than ever before, while even more digital content is created each day than in the whole of human history up to the creation of the internet. Protests, social justice movements, and political campaigns are won or lost through online organization and the dissemination of viral digital media. And this list, the list of influences digital media holds in our lives, still continues to grow and grow. In the vast interconnected global village of the internet, who are we in relationship to and how do we thoughtfully read digital media? And, even more, what skills are most useful to strengthen our careers and communities, and lead our own creative endeavors and social movements in this media landscape? These are some of the primary questions we will be exploring in this digital practices course.</p>


					<ul>
						<li>Our course explores digital media as an art form, bringing it together with design, cultural and technology studies, and into an interactive, collaborative class.</li>


						<li>Our course is oriented toward making, writing, building and identifying relevant skills for a digital media toolkit and historical and theoretical introductions to practices that will be useful for creative and critical thinkers working across disciplines.</li>


						<li>This course is required for BA in IDEAS, BA in Theatre and Performance, and BA in Theatre Design, Production and Technology. Extensive computer use (and internet access) required.</li>
					</ul>


					<h3><a name="courseOutcomes">Course Outcomes</a></h3>


					<ul>
						<li>Students will demonstrate a basic understanding of how key ideas of digital media practices in design and the arts are materialized through current digital media creation tools and their underling social and technological systems.</li>


						<li>Students will demonstrate an understanding of digital media resources in design and the arts.</li>


						<li>Students will demonstrate an ability to use current creative digital media tools (purpose built or appropriated) and systems in design and the arts.</li>


						<li>Students will demonstrate a basic understanding of how digital media practices in design and the arts (including architectural, visual, environmental, theatrical, musical, and art historical) reflects and shapes technological and cultural conditions, including questions of community, personal narrative, privacy, and security.</li>
					</ul>


					<h3><a name="assessmentMethods">Assessment Methods</a></h3>


					<ul>
						<li>Short projects, class readings, and class discussion will evaluate students’ understanding of how key ideas of digital media practices in design and arts disciplines are materialized through current digital tools and systems.</li>


						<li>Short projects will evaluate students’ understanding of digital media resources in design and arts disciplines.</li>


						<li>Short projects will evaluate the students’ ability to use current digital media tools and systems in design and arts disciplines.</li>


						<li>A website project will evaluate students’ ability to apply computer programming concepts.</li>


						<li>Assigned readings, videos to watch, audio to listen to, and weekly discussions will challenge students to think critically about how digital media practices in design and the arts shape technological and cultural conditions in relation to questions of community, personal narrative, privacy, and security.</li>
					</ul>


